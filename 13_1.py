"""Реализовать программу для бесконечной циклической
последовательности чисел (например, 1-2-3-1-2-3-1-2...).
Последовательность реализовать с помощью генераторной
функции, количество чисел для вывода задаётся
пользователем с клавиатуры.
"""
def cyclic_sequence(numbers):
    """Генераторная функция для бесконечной циклической последовательности чисел из списка `numbers`."""
    while True:
        for num in numbers:
            yield num

# Получаем количество чисел для вывода от пользователя
try:
    n = int(input("Введите количество чисел для вывода: "))
    if n <= 0:
        raise ValueError("Количество чисел должно быть положительным.")
except ValueError as e:
    print("Ошибка:", e)
else:
    # Инициализируем генератор
    sequence = cyclic_sequence([1, 2, 3])

    # Выводим нужное количество чисел из последовательности
    for _ in range(n):
        print(next(sequence), end=' ')
